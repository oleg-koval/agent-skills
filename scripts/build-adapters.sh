#!/bin/sh
set -eu

node <<'EOF'
const fs = require('fs')
const path = require('path')

const root = process.cwd()
const catalogPath = path.join(root, 'catalog', 'skills.json')
const catalog = JSON.parse(fs.readFileSync(catalogPath, 'utf8'))
const packageJson = JSON.parse(fs.readFileSync(path.join(root, 'package.json'), 'utf8'))
const projectName = catalog.name
const claudeMarketplaceName = 'olko-agent-skills'
const lookupNameFor = (pkg) => pkg.lookupName || pkg.name
const skillPathFor = (pkg) => path.join(root, pkg.path, 'SKILL.md')
const promptNameFor = (pkg) => `${pkg.name}.prompt.md`
const generatedHeader = '<!-- Generated by scripts/build-adapters.sh. Do not edit directly. -->'
const stripFrontmatter = (content) => content.replace(/^---\n[\s\S]*?\n---\n?/, '')
const extractFrontmatter = (content) => {
  const m = content.match(/^(---\n[\s\S]*?\n---\n?)/)
  if (!m) return ''
  return m[1].endsWith('\n') ? m[1] : m[1] + '\n'
}
const injectTargets = (content, targets) => {
  const targetsYaml = targets.length > 0
    ? `  targets: [${targets.map(t => JSON.stringify(t)).join(', ')}]`
    : '  targets: []'
  if (/^  targets:/m.test(content)) {
    return content.replace(/^  targets:.*$/m, targetsYaml)
  }
  return content.replace(/^(metadata:)$/m, `$1\n${targetsYaml}`)
}
const stripTrailingLineWhitespace = (content) => content.replace(/[ \t]+$/gm, '')
const copySkillResources = (pkg, destDir) => {
  for (const resourceDir of ['scripts']) {
    const source = path.join(root, pkg.path, resourceDir)
    const dest = path.join(destDir, resourceDir)
    if (!fs.existsSync(source)) {
      continue
    }
    fs.rmSync(dest, { recursive: true, force: true })
    fs.cpSync(source, dest, { recursive: true })
  }
}

const claudeSkillPaths = catalog.packages
  .filter((pkg) => pkg.adapters.includes('claude'))
  .map((pkg) => `./${pkg.path}`)

const plugins = [
  {
    name: claudeMarketplaceName,
    source: './',
    description: 'Agent-agnostic skill catalog for Codex, Claude, Cursor, Grok, Copilot, Windsurf, Kiro, and other skill-aware tools.',
  },
]

const cursorPlugins = catalog.packages
  .filter((pkg) => pkg.adapters.includes('cursor'))
  .map((pkg) => ({
    name: lookupNameFor(pkg),
    source: `./${pkg.path}/adapters/cursor`,
    description: pkg.description,
  }))

const grokPlugins = catalog.packages
  .filter((pkg) => pkg.adapters.includes('grok'))
  .map((pkg) => ({
    name: lookupNameFor(pkg),
    source: `./${pkg.path}/adapters/grok`,
    description: pkg.description,
  }))

fs.mkdirSync(path.join(root, '.claude-plugin'), { recursive: true })
fs.mkdirSync(path.join(root, '.cursor-plugin'), { recursive: true })
fs.mkdirSync(path.join(root, '.grok-plugin'), { recursive: true })
fs.mkdirSync(path.join(root, '.github', 'prompts'), { recursive: true })
fs.mkdirSync(path.join(root, '.windsurf', 'rules'), { recursive: true })
fs.mkdirSync(path.join(root, '.kiro', 'steering'), { recursive: true })

fs.writeFileSync(
  path.join(root, '.claude-plugin', 'marketplace.json'),
  JSON.stringify(
    {
      name: claudeMarketplaceName,
      owner: { name: 'Oleg Koval' },
      metadata: {
        description: 'Agent-agnostic skill catalog for Codex, Claude, Cursor, Grok, Copilot, Windsurf, Kiro, and other skill-aware tools.',
      },
      plugins,
    },
    null,
    2,
  ) + '\n',
)

fs.writeFileSync(
  path.join(root, '.cursor-plugin', 'index.json'),
  JSON.stringify(
    {
      name: projectName,
      plugins: cursorPlugins,
    },
    null,
    2,
  ) + '\n',
)

fs.writeFileSync(
  path.join(root, '.grok-plugin', 'index.json'),
  JSON.stringify(
    {
      name: projectName,
      plugins: grokPlugins,
    },
    null,
    2,
  ) + '\n',
)

fs.writeFileSync(
  path.join(root, '.claude-plugin', 'plugin.json'),
  JSON.stringify(
    {
      name: claudeMarketplaceName,
      description: 'Agent-agnostic skill catalog for Codex, Claude, Cursor, Grok, Copilot, Windsurf, Kiro, and other skill-aware tools.',
      version: packageJson.version,
      author: { name: 'Oleg Koval' },
      homepage: 'https://github.com/oleg-koval/agent-skills',
      repository: 'https://github.com/oleg-koval/agent-skills',
      license: 'MIT',
      skills: claudeSkillPaths,
    },
    null,
    2,
  ) + '\n',
)

const copilotInstructions = [
  generatedHeader,
  '',
  '# Agent Skills Repository Instructions',
  '',
  'This repository is an opinionated agent skill catalog. Skills encode concrete defaults, preferred tools, and repeatable workflows.',
  '',
  '- Treat `catalog/skills.json` as the source of truth for package metadata.',
  '- Keep canonical skill instructions in `packages/{category}/{skill}/SKILL.md`.',
  '- Keep generated adapter files in sync by running `./scripts/build-adapters.sh` after catalog or skill metadata changes (includes Cursor `skills/*/SKILL.md` copies from each canonical `SKILL.md`).',
  '- Run `./scripts/validate-catalog.sh` before release.',
  '- Prefer small, scoped updates that preserve existing package structure.',
  '',
  'Available reusable prompt files:',
  ...catalog.packages.filter((pkg) => pkg.adapters.includes('copilot')).map((pkg) => `- ${pkg.name}: .github/prompts/${promptNameFor(pkg)}`),
  '',
].join('\n')

fs.writeFileSync(path.join(root, '.github', 'copilot-instructions.md'), copilotInstructions)

for (const pkg of catalog.packages) {
  if (!pkg.adapters.includes('copilot')) {
    continue
  }

  const skillBody = stripFrontmatter(fs.readFileSync(skillPathFor(pkg), 'utf8')).trimStart().trimEnd()
  const prompt = stripTrailingLineWhitespace([
    generatedHeader,
    '',
    '---',
    `description: ${JSON.stringify(pkg.description)}`,
    '---',
    '',
    `Use the ${lookupNameFor(pkg)} skill.`,
    '',
    skillBody,
    '',
  ].join('\n'))

  fs.writeFileSync(path.join(root, '.github', 'prompts', promptNameFor(pkg)), prompt)
}

for (const pkg of catalog.packages) {
  if (!pkg.adapters.includes('windsurf')) {
    continue
  }

  const skillBody = stripFrontmatter(fs.readFileSync(skillPathFor(pkg), 'utf8')).trimStart().trimEnd()
  const content = stripTrailingLineWhitespace([
    generatedHeader,
    '',
    '---',
    `description: ${JSON.stringify(pkg.description)}`,
    '---',
    '',
    skillBody,
    '',
  ].join('\n'))

  fs.writeFileSync(path.join(root, '.windsurf', 'rules', `${pkg.name}.md`), content)
}

for (const pkg of catalog.packages) {
  if (!pkg.adapters.includes('kiro')) {
    continue
  }

  const skillBody = stripFrontmatter(fs.readFileSync(skillPathFor(pkg), 'utf8')).trimStart().trimEnd()
  const content = stripTrailingLineWhitespace([
    generatedHeader,
    '',
    '---',
    'inclusion: manual',
    `description: ${JSON.stringify(pkg.description)}`,
    '---',
    '',
    skillBody,
    '',
  ].join('\n'))

  fs.writeFileSync(path.join(root, '.kiro', 'steering', `${pkg.name}.md`), content)
}

for (const pkg of catalog.packages) {
  const canonical = fs.readFileSync(skillPathFor(pkg), 'utf8').trimEnd()
  const fm = extractFrontmatter(canonical)
  if (!fm) throw new Error(`canonical SKILL.md has no frontmatter: ${skillPathFor(pkg)}`)
  const body = stripTrailingLineWhitespace(stripFrontmatter(canonical).trimStart().trimEnd())

  if (pkg.adapters.includes('claude')) {
    const claudeDestDir = path.join(root, pkg.path, 'adapters', 'claude', 'skills', pkg.name)
    fs.mkdirSync(claudeDestDir, { recursive: true })
    fs.writeFileSync(
      path.join(root, pkg.path, 'adapters', 'claude', 'plugin.json'),
      JSON.stringify(
        {
          name: `olko-${pkg.name}`,
          description: pkg.description,
          skills: './skills',
        },
        null,
        2,
      ) + '\n',
    )
    fs.writeFileSync(path.join(claudeDestDir, 'SKILL.md'), [fm + generatedHeader, '', body, ''].join('\n'))
    copySkillResources(pkg, claudeDestDir)
  }

  if (pkg.adapters.includes('cursor')) {
    const cursorAdapterDir = path.join(root, pkg.path, 'adapters', 'cursor')
    const destDir = path.join(cursorAdapterDir, 'skills', pkg.name)
    fs.mkdirSync(destDir, { recursive: true })
    fs.writeFileSync(
      path.join(cursorAdapterDir, 'plugin.json'),
      JSON.stringify(
        {
          name: lookupNameFor(pkg),
          version: '0.1.0',
          description: pkg.description,
          skills: 'skills/',
        },
        null,
        2,
      ) + '\n',
    )
    const cursorCanonical = injectTargets(canonical, ['cursor'])
    const cursorFrontmatter = extractFrontmatter(cursorCanonical)
    const cursorBody = stripTrailingLineWhitespace(stripFrontmatter(cursorCanonical).trimStart().trimEnd())
    fs.writeFileSync(path.join(destDir, 'SKILL.md'), [cursorFrontmatter + generatedHeader, '', cursorBody, ''].join('\n'))
    copySkillResources(pkg, destDir)
  }

  if (pkg.adapters.includes('grok')) {
    const grokAdapterDir = path.join(root, pkg.path, 'adapters', 'grok')
    const destDir = path.join(grokAdapterDir, 'skills', pkg.name)
    fs.mkdirSync(destDir, { recursive: true })
    fs.writeFileSync(
      path.join(grokAdapterDir, 'plugin.json'),
      JSON.stringify(
        {
          name: lookupNameFor(pkg),
          version: '0.1.0',
          description: pkg.description,
          skills: 'skills/',
        },
        null,
        2,
      ) + '\n',
    )
    // Grok uses standard SKILL.md (no special targets injection like cursor)
    fs.writeFileSync(path.join(destDir, 'SKILL.md'), [fm + generatedHeader, '', body, ''].join('\n'))
    copySkillResources(pkg, destDir)
  }

  if (pkg.adapters.includes('windsurf')) {
    const skillBody = stripFrontmatter(canonical).trimStart().trimEnd()
    const content = stripTrailingLineWhitespace([
      generatedHeader,
      '',
      '---',
      `description: ${JSON.stringify(pkg.description)}`,
      '---',
      '',
      skillBody,
      '',
    ].join('\n'))
    const destDir = path.join(root, pkg.path, 'adapters', 'windsurf', 'rules')
    fs.mkdirSync(destDir, { recursive: true })
    fs.writeFileSync(path.join(destDir, `${pkg.name}.md`), content)
  }

  if (pkg.adapters.includes('kiro')) {
    const skillBody = stripFrontmatter(canonical).trimStart().trimEnd()
    const content = stripTrailingLineWhitespace([
      generatedHeader,
      '',
      '---',
      'inclusion: manual',
      `description: ${JSON.stringify(pkg.description)}`,
      '---',
      '',
      skillBody,
      '',
    ].join('\n'))
    const destDir = path.join(root, pkg.path, 'adapters', 'kiro', 'steering')
    fs.mkdirSync(destDir, { recursive: true })
    fs.writeFileSync(path.join(destDir, `${pkg.name}.md`), content)
  }
}

console.log('generated marketplace manifests, Copilot prompts, Cursor, Grok, Windsurf, and Kiro adapter files')
EOF
